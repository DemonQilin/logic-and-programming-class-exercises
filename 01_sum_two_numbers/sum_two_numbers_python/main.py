def main():
    print("--PROGRAMA PARA LA SUMA DE DOS NUMEROS--\n")

    first_number = int(input("Ingrese el valor del primer número: "))
    second_number = int(input("Ingrese el valor del segundo número: "))

    total = first_number + second_number

    print(f"\nLa suma de {first_number} y {second_number} es: {total}")


if __name__ == '__main__':
    main()
